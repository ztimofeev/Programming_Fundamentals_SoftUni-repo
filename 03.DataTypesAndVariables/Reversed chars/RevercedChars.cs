
namespace Reversed_chars
{
    using System;

    public class RevercedChars
    {
        public static void Main()
        {
            var firstLetter = Console.ReadLine();
            var secondLetter = Console.ReadLine();
            var thirdLetter = Console.ReadLine();

            Console.WriteLine("{0}{1}{2}", thirdLetter, secondLetter, firstLetter);
        }
    }
}
